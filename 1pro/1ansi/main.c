// ANSI Escape Code를 이용한 콘솔 제어 함수들

// 1️⃣ 커서 이동 함수
// (x, y) 위치로 커서를 이동합니다. (y가 행, x가 열)
void gotoXY(int x, int y) {
    printf("\033[%d;%dH", y, x);
    printf("[예시] %d번째 줄, %d번째 칸으로 이동했습니다.\n", y, x);
}

// 2️⃣ 글자 색상 변경 함수
// ANSI 색상 코드를 사용해 글자 색상을 바꿉니다.
// 대표 색상 코드:
// 30=검정, 31=빨강, 32=초록, 33=노랑, 34=파랑, 35=자홍, 36=청록, 37=흰색
// 밝은 색상(90~97): 90=밝은 검정(회색), 91=밝은 빨강, 92=밝은 초록 등
void setTextColor(int color) {
    printf("\033[%dm", color);
    printf("[예시] 색상 코드 %d번으로 변경되었습니다!\n", color);
    printf("예) 31=빨강, 32=초록, 33=노랑, 34=파랑, 35=자홍, 36=청록, 37=흰색\n");
    printf("    90~97은 밝은 색 계열입니다.\n");
}

// 3️⃣ 색상 초기화 함수
// 글자 색상/스타일을 기본값으로 되돌립니다.
void resetTextColor() {
    printf("\033[0m");
    printf("[예시] 색상과 스타일이 초기화되었습니다.\n");
}

// 4️⃣ 밑줄 시작 함수
// 이후 출력되는 글자에 밑줄을 표시합니다.
void startUnderline() {
    printf("\033[4m");
    printf("[예시] 밑줄이 시작됩니다. 예) _이 글자에 밑줄_\n");
}

// 5️⃣ 밑줄 종료 함수
// 이후 출력되는 글자에서 밑줄을 제거합니다.
void endUnderline() {
    printf("\033[24m");
    printf("[예시] 밑줄이 종료됩니다.\n");
}
