// 기출문제 첫번째 별 네모 만들기

#include <stdio.h>
#include <windows.h>  // Sleep() 함수 사용

int main() {
    int i = 0;

    // 위쪽 가로줄 별 5개 출력 (맨 윗줄, 한 줄에 별을 5개 출력)
    while (i < 5) {
        printf("*");       // 현재 커서 위치에서 별 출력
        Sleep(1000);       // 1초 대기
        i++;               // 다음 별로 이동
    }

    // 오른쪽 세로줄 별 4개 출력 (위쪽 가로줄 마지막 별과 이어지도록)
    // 이미 위쪽 별이 있으므로 두 번째 줄(y=2)부터 네 번째 줄(y=5)까지 출력
    i = 0;
    while (i < 4) {
        printf("\033[%d;%dH", 2 + i, 5);  // (y, x): 2~5행, 5열
        printf("*");       // 해당 위치에서 별 출력
        Sleep(1000);       // 1초 대기
        i++;
    }

    // 아래쪽 가로줄 별 4개 출력 (오른쪽 별의 마지막 위치와 이어지도록)
    // y=5(맨 아래줄)에서 오른쪽에서 왼쪽으로 출력
    i = 0;
    while (i < 4) {
        printf("\033[%d;%dH", 5, 4 - i);  // (y, x): 5행, 4~1열
        printf("*");       // 해당 위치에서 별 출력
        Sleep(1000);       // 1초 대기
        i++;
    }

    // 왼쪽 세로줄 별 4개 출력 (아래쪽 별의 마지막 위치와 이어지도록)
    // 이미 아래쪽 별이 있으므로 아래에서 위로 출력
    i = 0;
    while (i < 4) {
        printf("\033[%d;%dH", 4 - i, 1);  // (y, x): 4~1행, 1열
        printf("*");       // 해당 위치에서 별 출력
        Sleep(1000);       // 1초 대기
        i++;
    }

    // 마지막으로 커서를 네모 바깥쪽으로 옮겨두기 (7행, 1열)
    printf("\033[7;1H");

    return 0;
}
